/**
 * 三角形
 */
public class Triangle {
    /** 三角形の底辺の長さ */
    private int base;

    /** 三角形の高さ */
    private int height;

    public int getBase() {
        return base;
    }

    public Triangle setBase(int base) {
        this.base = base;
        return this;
    }

    public int getHeight() {
        return height;
    }

    public Triangle setHeight(int height) {
        this.height = height;
        return this;
    }
}
